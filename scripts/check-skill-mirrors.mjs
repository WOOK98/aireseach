import { spawnSync } from "node:child_process";
import { access, readFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const pluginRoot =
  process.env.AIRESEARCH_PLUGIN_DIR ??
  resolve(repoRoot, "..", "airesearch-plugin");

const mirrors = [
  {
    source: resolve(pluginRoot, "skills/deep-dive/SKILL.md"),
    mirror: new URL("../skills/deep-dive/SKILL.md", import.meta.url),
    stripMirrorHeader: true,
  },
];

const stripMirrorHeader = (text) =>
  text.replace(
    /^---\n(?:# MIRROR OF .+\n# DO NOT EDIT THIS FILE DIRECTLY\.\n# Update the plugin repository first, then run:\n#   pnpm check:skill-mirrors\n)/,
    "---\n",
  );

const formatMarkdown = (text, filepath) => {
  const result = spawnSync(
    "pnpm",
    ["exec", "oxfmt", "--stdin-filepath", filepath],
    {
      input: text,
      encoding: "utf8",
    },
  );

  if (result.status !== 0) {
    console.error(result.stderr);
    process.exit(result.status ?? 1);
  }

  return result.stdout;
};

let failed = false;

for (const item of mirrors) {
  try {
    await access(item.source);
  } catch {
    console.error(`Skill source missing: ${item.source}`);
    console.error(
      "Set AIRESEARCH_PLUGIN_DIR to the airesearch-plugin checkout.",
    );
    process.exit(1);
  }

  const source = await readFile(item.source, "utf8");
  const mirrorRaw = await readFile(item.mirror, "utf8");
  const mirror = item.stripMirrorHeader
    ? stripMirrorHeader(mirrorRaw)
    : mirrorRaw;
  const sourceComparable = formatMarkdown(source, item.source);
  const mirrorComparable = formatMarkdown(mirror, item.mirror.pathname);

  if (sourceComparable !== mirrorComparable) {
    failed = true;
    console.error(`Skill mirror drift detected: ${item.mirror.pathname}`);
    console.error(`Source of truth: ${item.source}`);
  }
}

if (failed) {
  process.exit(1);
}

console.log("Skill mirrors are in sync.");
